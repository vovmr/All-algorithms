const ll mod = 1e9 + 7;

inline ll bp(ll a, ll n) {
	ll res = 1;
	while (n) {
		if (n & 1) res *= a, res %= mod;
		a *= a, a %= mod;
		n >>= 1;
	}
	return res;
}

void solve() {
	int n;
	cin >> n;
	vector <ll> iv(n); iv[0] = iv[1] = 1;
	for (ll i = 2; i < n; ++i) {
		iv[i] = (mod - (mod / i)) * iv[mod % i] % mod;
		/// faster then bp(i, mod - 2)
		assert((i * iv[i]) % mod == 1);
	}
}

